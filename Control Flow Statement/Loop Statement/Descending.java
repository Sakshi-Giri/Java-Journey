class Descending
{
	public static void main(String[] args) {
		for(char ch = 'Z'; ch >= 'A'; ch--)
		{
			System.out.print(ch +" ");
		}
		System.out.println(" ");

		System.out.println("*************************************");

		for(char ch = 122; ch >= 97; ch--)
		{
			System.out.print(ch +" ");
		}

		System.out.println(" ");

		System.out.println("*************************************");

		for(char ch = 57; ch >= 48; ch--)
		{
			System.out.print(ch +" ");
		}
	}
}